#include <iostream>
using namespace std;

int main()
{
  cout << "Single"; // The next output will be on the same line 
  cout << "Line\n" ;  // The next output will be on the next line due to new line symbol \n 
  cout << "Separate new line" << "\n"; // The new line symbol can be written separately
  cout << "Use of endl for new line" << endl; // The endl must be written separately
  cout << "It demonstrates the line break"; // This line is shown on a new line
  return 0;
}